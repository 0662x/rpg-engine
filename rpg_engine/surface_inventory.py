def default_mcp_violations(tool_names: tuple[str, ...] | None = None) -> tuple[str, ...]:
    names = tool_names or mcp_default_tool_names()
    violations: list[str] = []
    for name in names:
        lower = name.lower()
        if any(token in lower for token in FORBIDDEN_DEFAULT_MCP_TOKENS):
            violations.append(name)
    return tuple(violations)


def validate_surface_inventory(entries: tuple[SurfaceEntry, ...] | None = None) -> tuple[str, ...]:
    inventory = all_surface_entries() if entries is None else entries
    errors: list[str] = []
    seen: set[tuple[str, str]] = set()
    seen_public_cli_names: set[str] = set()
    for entry in inventory:
        key = (entry.surface, entry.name)
        label = f"{entry.surface}:{entry.name}"
        if key in seen:
            errors.append(f"{label}: duplicate surface entry")
        seen.add(key)
        if entry.name.startswith("aigm "):
            if entry.name in seen_public_cli_names:
                errors.append(f"{label}: duplicate public CLI surface entry")
            seen_public_cli_names.add(entry.name)
        if not entry.taxonomy_category:
            errors.append(f"{label}: missing taxonomy_category")
        elif entry.taxonomy_category not in CANONICAL_TAXONOMY_CATEGORIES:
            errors.append(f"{label}: invalid taxonomy_category {entry.taxonomy_category!r}")
        if not entry.write_authority.strip():
            errors.append(f"{label}: missing write_authority")
        if not entry.intended_caller.strip():
            errors.append(f"{label}: missing intended_caller")
        if isinstance(entry.forbidden_bypasses, str) or not entry.forbidden_bypasses or any(
            not isinstance(item, str) or not item.strip() or item.strip().lower() == "none"
            for item in entry.forbidden_bypasses
        ):
            errors.append(f"{label}: missing forbidden_bypasses")
        if _requires_authority_gate(entry) and not entry.authority_gate.strip():
            errors.append(f"{label}: missing authority_gate")
        if entry.surface == "mcp" and entry.default_exposed and entry.taxonomy_category != "player-safe":
            errors.append(f"{label}: default-exposed MCP entry must be player-safe")
        if entry.taxonomy_category == "player-safe" and _exposes_low_level_authority(entry):
            errors.append(f"{label}: player-safe entry exposes low-level authority")
    return tuple(errors)


def _requires_authority_gate(entry: SurfaceEntry) -> bool:
    mode = entry.write_mode.lower()
    return any(token in mode for token in AUTHORITY_GATE_REQUIRED_WRITE_MODE_TOKENS)


def _exposes_low_level_authority(entry: SurfaceEntry) -> bool:
    low_level_terms = (
        "developer",
        "trusted_gm",
        "maintenance",
        "admin",
        "admin_write",
        "maintenance_write",
        "turn_low_level",
        "low-level",
        "direct low-level",
        "commit_turn",
        "gmruntime.commit_turn",
        "turnproposal",
        "raw delta",
        "validate_delta",
    )
    haystack = " ".join(
        [
            entry.name,
            entry.category,
            entry.profile,
            entry.write_mode,
            entry.write_authority,
            entry.authority_gate,
            " ".join(entry.notes),
        ]
    ).lower()
    if any(term in haystack for term in low_level_terms):
        return True
    if "commit" in entry.write_mode.lower():
        player_confirm_path = "savemanager.player_confirm" in haystack or "pending player action" in haystack
        return not player_confirm_path
    return False


