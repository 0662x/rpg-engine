def _cli_v1_subcommand_entry(
    command: str,
    *,
    category: str,
    taxonomy_category: str,
    profile: str,
    write_mode: str,
    write_authority: str,
    intended_caller: str,
    description: str,
    forbidden_bypasses: tuple[str, ...],
    authority_gate: str = "",
    default_exposed: bool = False,
    normal_play: bool = False,
) -> SurfaceEntry:
    return SurfaceEntry(
        name=f"aigm {command}",
        surface="cli_v1",
        category=category,
        taxonomy_category=taxonomy_category,
        profile=profile,
        write_mode=write_mode,
        write_authority=write_authority,
        intended_caller=intended_caller,
        default_exposed=default_exposed,
        normal_play=normal_play,
        description=description,
        forbidden_bypasses=forbidden_bypasses,
        authority_gate=authority_gate,
    )


