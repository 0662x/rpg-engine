@dataclass(frozen=True)
class SurfaceEntry:
    name: str
    surface: str
    category: str
    profile: str
    write_mode: str
    default_exposed: bool
    normal_play: bool
    description: str
    notes: tuple[str, ...] = ()
    related_tools: tuple[str, ...] = ()
    taxonomy_category: str = ""
    write_authority: str = ""
    intended_caller: str = ""
    forbidden_bypasses: tuple[str, ...] = ()
    authority_gate: str = ""

    def to_dict(self) -> dict[str, Any]:
        return asdict(self)


