def write_cards(
    campaign: Campaign,
    conn: sqlite3.Connection,
    *,
    clean: bool = True,
    index_view: str = PLAYER_VIEW,
) -> list[Path]:
    registry = get_default_card_registry()
    cards_root = campaign.cards_path
    cards_root.mkdir(parents=True, exist_ok=True)
    ensure_visibility_sql_functions(conn)
    entity_visibility_clause = entity_visibility_sql(index_view, "e")
    subtype_visibility_clause = entity_subtype_visibility_sql(index_view, "e", "c")

    rows = conn.execute(
        f"""
        select e.*
        from entities e
        left join clocks c on c.entity_id = e.id
        where {entity_not_archived_sql("e")}
          {entity_visibility_clause}
          {subtype_visibility_clause}
        order by name
        """
    ).fetchall()
    rows = sorted(rows, key=lambda row: card_sort_key(row, registry))

    written: list[Path] = []
    target_paths: set[Path] = set()
    for row in rows:
        relative_path = card_relative_path(row, registry)
        path = cards_root / relative_path
        path.parent.mkdir(parents=True, exist_ok=True)
        write_text_atomic(path, render_card(conn, row, registry) + "\n")
        written.append(path)
        target_paths.add(path.resolve())

    index_path = cards_root / "INDEX.md"
    write_text_atomic(index_path, render_card_index(campaign, conn, rows, view=index_view, registry=registry) + "\n")
    written.insert(0, index_path)
    target_paths.add(index_path.resolve())
    if clean:
        remove_generated_cards(cards_root, keep=target_paths)
    return written


def remove_generated_cards(cards_root: Path, *, keep: set[Path] | None = None) -> None:
    keep = keep or set()
    for path in cards_root.rglob("*.md"):
        if path.resolve() in keep:
            continue
        if path.name == "INDEX.md":
            path.unlink()
            continue
        try:
            first_line = path.read_text(encoding="utf-8").splitlines()[0]
        except (OSError, IndexError, UnicodeDecodeError):
            continue
        if first_line == GENERATED_MARKER:
            path.unlink()


def card_relative_path(entity: sqlite3.Row, registry: CardRegistry | None = None) -> Path:
    directory = card_directory(str(entity["type"]), registry)
    return Path(directory) / f"{safe_filename(entity['id'])}.md"


def card_directory(entity_type: str, registry: CardRegistry | None = None) -> str:
    registry = registry or get_default_card_registry()
    return registry.card_dir(entity_type)


def card_sort_key(entity: sqlite3.Row, registry: CardRegistry | None = None) -> tuple[int, str, str]:
    registry = registry or get_default_card_registry()
    return registry.sort_key(str(entity["type"]), str(entity["name"] or ""))


def safe_filename(value: str) -> str:
    text = value.replace(":", "__")
    text = re.sub(r"[^0-9A-Za-z._-]+", "_", text)
    return text.strip("._") or "entity"


def render_card(conn: sqlite3.Connection, entity: sqlite3.Row, registry: CardRegistry | None = None) -> str:
    registry = registry or get_default_card_registry()
    type_name = entity["type"]
    hidden_ids = hidden_entity_ids(conn)
    location_id = redact_hidden_refs(entity["location_id"] or "无", hidden_ids) or "无"
    owner_id = redact_hidden_refs(entity["owner_id"] or "无", hidden_ids) or "无"
    details = redact_hidden_refs(parse_json(entity["details_json"], {}), hidden_ids)
    aliases = get_aliases(conn, entity["id"])
    lines = [
        GENERATED_MARKER,
        f"# {entity['id']} · {entity['name']}",
        "",
        "| 字段 | 值 |",
        "|------|----|",
        f"| 类型 | {display_label('entity_type', type_name)} |",
        f"| 状态 | {display_label('status', entity['status'])} |",
        f"| 可见性 | {display_label('visibility', entity['visibility'])} |",
        f"| 位置 | {location_id} |",
        f"| 所有者 | {owner_id} |",
        f"| 更新回合 | {entity['updated_turn_id']} |",
    ]
    if aliases:
        lines.append(f"| 别名 | {'；'.join(aliases)} |")
    summary = redact_hidden_refs(entity["summary"] or "无", hidden_ids) or "无"
    lines.extend(["", "## 快速摘要", shorten(summary, 420)])

    spec = registry.by_entity_type(str(type_name))
    if spec and spec.append_sections:
        spec.append_sections(conn, lines, entity)
    else:
        append_details_card(lines, details)

    append_active_facts(conn, lines, entity["id"])
    lines.extend(
        [
            "",
            "## 查询提示",
            "- 查询这个对象时优先读本卡，再按需查 SQLite。",
            "- 行动回合若改变本对象，必须写入结构化 delta 并重新生成卡片。",
        ]
    )
    return str(redact_hidden_refs("\n".join(lines), hidden_ids))


def append_item_card(conn: sqlite3.Connection, lines: list[str], entity: sqlite3.Row) -> None:
    item = conn.execute("select * from items where entity_id = ?", (entity["id"],)).fetchone()
    hidden_ids = hidden_entity_ids(conn)
    details = redact_hidden_refs(parse_json(entity["details_json"], {}), hidden_ids)
    properties = redact_hidden_refs(parse_json(item["properties_json"], {}) if item else {}, hidden_ids)
    if item:
        durability = "不适用"
        if item["durability_current"] is not None or item["durability_max"] is not None:
            durability = f"{item['durability_current'] or '?'} / {item['durability_max'] or '?'}"
        lines.extend(
            [
                "",
                "## 物品属性",
                "| 字段 | 值 |",
                "|------|----|",
                f"| 分类 | {display_label('item_category', item['category'])} |",
                f"| 数量 | {format_quantity(item)} |",
                f"| 品质 | {display_label('quality', item['quality'], default='未知')} |",
                f"| 耐久 | {durability} |",
                f"| 装备槽 | {item['equipped_slot'] or '无'} |",
                f"| 可堆叠 | {'是' if item['stackable'] else '否'} |",
            ]
        )
    append_item_profile_sections(lines, properties, heading_level=2)
    append_mapping(
        lines,
        "## 关键属性",
        properties,
        exclude={"combat_profile", "ammo_profile", "melee_profile", "defense_profile", "carry_profile"},
    )
    append_details_card(lines, details, exclude={"properties"})


def append_character_card(conn: sqlite3.Connection, lines: list[str], entity: sqlite3.Row) -> None:
    char = conn.execute("select * from characters where entity_id = ?", (entity["id"],)).fetchone()
    hidden_ids = hidden_entity_ids(conn)
    details = redact_hidden_refs(parse_json(entity["details_json"], {}), hidden_ids)
    profile = details.get("profile")
    if char:
        species_id = redact_hidden_refs(char["species_id"] or "未知", hidden_ids) or "未知"
        lines.extend(
            [
                "",
                "## 角色状态",
                "| 字段 | 值 |",
                "|------|----|",
                f"| 种族 | {species_id} |",
                f"| 角色定位 | {format_value(char['role']) if char['role'] else '未知'} |",
                f"| 态度 | {format_value(char['attitude']) if char['attitude'] else '未知'} |",
                f"| 信任 | {char['trust']} |",
                f"| 健康 | {format_value(char['health_state']) if char['health_state'] else '未知'} |",
            ]
        )
        append_mapping(lines, "## 压力", redact_hidden_refs(parse_json(char["stress_json"], {}), hidden_ids), exclude=set())
        append_sequence(lines, "## 长期后果", redact_hidden_refs(parse_json(char["consequences_json"], []), hidden_ids))
        append_sequence(lines, "## 目标", redact_hidden_refs(parse_json(char["goals_json"], []), hidden_ids))
    if isinstance(profile, dict):
        selected_profile = {key: profile[key] for key in CHARACTER_PROFILE_KEYS if key in profile}
        append_mapping(lines, "## 人物资料", selected_profile, exclude=set())
        omitted = len([key for key in profile if key not in selected_profile])
        if omitted:
            lines.append(f"- 另有 {omitted} 项低频资料留在 SQLite/source archive，需要时再查。")
    append_sequence(lines, "## 已知能力", details.get("known_abilities", []))
    append_sequence(lines, "## 当前承诺", details.get("commitments", []))
    append_sequence(lines, "## 未确认信息", details.get("unknowns", []))
    append_details_card(
        lines,
        details,
        exclude={"known_abilities", "commitments", "unknowns", "profile", "profile_excerpt"},
    )


