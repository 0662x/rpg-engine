def append_details_card(lines: list[str], details: dict[str, Any], exclude: set[str] | None = None) -> None:
    append_mapping(lines, "## 细节", details, exclude=exclude or set())


def append_mapping(lines: list[str], title: str, values: dict[str, Any], *, exclude: set[str]) -> None:
    items = [(key, value) for key, value in values.items() if key not in exclude and value not in (None, "", [], {})]
    if not items:
        return
    lines.extend(["", title])
    for key, value in items[:24]:
        lines.append(f"- {display_key(key)}: {shorten(format_value(value), 360)}")
    if len(items) > 24:
        lines.append(f"- 另有 {len(items) - 24} 项细节已省略；需要时查 SQLite。")


def append_sequence(lines: list[str], title: str, values: Any) -> None:
    if not values:
        return
    if not isinstance(values, list):
        values = [values]
    lines.extend(["", title])
    for value in values[:24]:
        lines.append(f"- {shorten(format_value(value), 360)}")
    if len(values) > 24:
        lines.append(f"- 另有 {len(values) - 24} 项已省略；需要时查 SQLite。")


def append_active_facts(conn: sqlite3.Connection, lines: list[str], entity_id: str) -> None:
    hidden_ids = hidden_entity_ids(conn)
    facts = conn.execute(
        """
        select predicate, object_entity_id, object_value, value_type, confidence, note
        from facts
        where subject_id = ? and valid_to_turn is null
        order by predicate
        limit 20
        """,
        (entity_id,),
    ).fetchall()
    if not facts:
        return
    lines.extend(["", "## 明确事实", "| 谓词 | 值 | 置信度 | 备注 |", "|------|----|--------|------|"])
    for fact in facts:
        value = fact["object_entity_id"] or fact["object_value"] or ""
        value = redact_hidden_refs(value, hidden_ids) or ""
        note = redact_hidden_refs(fact["note"] or "", hidden_ids) or ""
        lines.append(
            f"| {display_key(fact['predicate'])} | {format_value(value)} ({display_label('entity_type', fact['value_type'])}) | "
            f"{format_value(fact['confidence'])} | {note} |"
        )


def render_card_index(
    campaign: Campaign,
    conn: sqlite3.Connection,
    entities: list[sqlite3.Row],
    *,
    view: str = PLAYER_VIEW,
    registry: CardRegistry | None = None,
) -> str:
    view = normalize_visibility_view(view)
    registry = registry or get_default_card_registry()
    ensure_visibility_sql_functions(conn)
    meta = get_meta(conn)
    visible_entities = sorted(
        [row for row in entities if read_entity(conn, str(row["id"]), view=view)],
        key=lambda row: card_sort_key(row, registry),
    )
    counts: dict[str, int] = {}
    for row in visible_entities:
        counts[str(row["type"])] = counts.get(str(row["type"]), 0) + 1
    current_location_id = meta.get("current_location_id", "")
    entity_visibility_clause = entity_visibility_sql(view, "e")
    subtype_visibility_clause = entity_subtype_visibility_sql(view, "e", "c")
    current_location = conn.execute(
        f"""
        select e.name
        from entities e
        left join clocks c on c.entity_id = e.id
        where e.id = ?
          and {normalized_text_sql("e.type")} = 'location'
          and {entity_not_archived_sql("e")}
          {entity_visibility_clause}
          {subtype_visibility_clause}
        """,
        (current_location_id,),
    ).fetchone()
    player_entity_id = get_player_entity_id(conn)
    carried = conn.execute(
        f"""
        select e.*
        from entities e
        join items i on i.entity_id = e.id
        left join clocks c on c.entity_id = e.id
        where e.owner_id = ? and {normalized_text_sql("e.status")} = 'active'
          {entity_visibility_clause}
          {subtype_visibility_clause}
        order by i.equipped_slot is null, i.equipped_slot, e.name
        limit 20
        """,
        (player_entity_id,),
    ).fetchall()
    clock_visibility_clause = clock_visibility_sql(view, "c")
    clocks = conn.execute(
        f"""
        select e.*, c.segments_filled, c.segments_total, c.visibility as clock_visibility
        from entities e
        join clocks c on c.entity_id = e.id
        where {entity_not_archived_sql("e")}
          {clock_visibility_clause}
          {entity_visibility_clause}
          {subtype_visibility_clause}
        order by c.visibility, e.name
        """
    ).fetchall()

    lines = [
        GENERATED_MARKER,
        f"# {campaign.name} 资料卡索引",
        "",
        "| 字段 | 值 |",
        "|------|----|",
        f"| 当前回合 | {meta.get('current_turn_id', 'unknown')} |",
        f"| 游戏时间 | {format_time_brief(meta)} |",
        f"| 天气 | {format_weather_brief(meta)} |",
        f"| 索引视角 | {display_label('view', view)} |",
        f"| 当前位置 | {card_link(current_location_id, current_location['name'], 'location') if current_location else '当前地点不可见或不存在'} |",
        "",
        "## 实体统计",
        "| 类型 | 数量 |",
        "|------|------|",
    ]
    for entity_type in ordered_entity_types(counts.keys(), registry):
        lines.append(f"| {display_label('entity_type', entity_type)} | {counts[entity_type]} |")

    lines.extend(["", "## 当前随身重点"])
    if carried:
        for row in carried:
            lines.append(f"- {card_link(row['id'], row['name'], row['type'])} `{display_label('entity_type', row['type'])}`")
    else:
        lines.append("- 无")

    lines.extend(["", "## 活跃进度钟"])
    if clocks:
        for row in clocks:
            filled = int(row["segments_filled"])
            total = int(row["segments_total"])
            bar = "■" * filled + "□" * max(total - filled, 0)
            lines.append(
                f"- {card_link(row['id'], row['name'], row['type'])}: "
                f"{bar} {filled}/{total} `{display_label('visibility', row['clock_visibility'])}`"
            )
    else:
        lines.append("- 无")

    by_type: dict[str, list[sqlite3.Row]] = {}
    for row in visible_entities:
        by_type.setdefault(row["type"], []).append(row)
    for entity_type in ordered_entity_types(by_type.keys(), registry):
        spec = registry.by_entity_type(entity_type)
        title = spec.title if spec and spec.index_title else display_label("entity_type", entity_type)
        lines.extend(["", f"## {title}"])
        for row in by_type[entity_type]:
            summary = shorten(redact_hidden_refs(row["summary"] or "", hidden_entity_ids(conn)) or "", 110)
            suffix = f" - {summary}" if summary else ""
            lines.append(f"- {card_link(row['id'], row['name'], row['type'])}{suffix}")

    lines.extend(
        [
            "",
            "## 使用规则",
            "- 新会话优先读 `snapshots/current.md`、本索引和相关实体卡。",
            "- 查询类输入不推进时间；行动类输入必须写结构化 delta 并重新生成快照和卡片。",
            "- 卡片是快速上下文，权威事实仍以 `data/game.sqlite` 为准。",
        ]
    )
    text = "\n".join(lines)
    if view == PLAYER_VIEW:
        return str(redact_hidden_refs(text, hidden_entity_ids(conn)))
    return text


def ordered_entity_types(entity_types: Any, registry: CardRegistry | None = None) -> list[str]:
    registry = registry or get_default_card_registry()
    return sorted([str(entity_type) for entity_type in entity_types], key=lambda entity_type: registry.sort_key(entity_type, ""))


def card_link(entity_id: str, name: str, entity_type: str | None = None, registry: CardRegistry | None = None) -> str:
    registry = registry or get_default_card_registry()
    directory = card_directory(entity_type or entity_type_from_id(entity_id, registry), registry)
    path = Path(directory) / f"{safe_filename(entity_id)}.md"
    return f"[{name}]({path.as_posix()})"


def entity_type_from_id(entity_id: str, registry: CardRegistry | None = None) -> str:
    registry = registry or get_default_card_registry()
    return registry.entity_type_from_id(entity_id)


def get_aliases(conn: sqlite3.Connection, entity_id: str) -> list[str]:
    return [
        row["alias"]
        for row in conn.execute(
            "select alias from aliases where entity_id = ? order by alias",
            (entity_id,),
        ).fetchall()
    ]


def hidden_entity_ids(conn: sqlite3.Connection) -> set[str]:
    refs = hidden_entity_refs(conn)
    return {token for values in refs.values() for token in values}


def redact_hidden_refs(value: Any, hidden_ids: set[str]) -> Any:
    return redact_entity_refs(value, hidden_ids)


def format_coords(location: sqlite3.Row) -> str:
    coords = [location["coord_x"], location["coord_y"], location["coord_z"]]
    if all(value is None for value in coords):
        return "未知"
    return ", ".join("?" if value is None else str(value) for value in coords)


def shorten(text: str, limit: int) -> str:
    compact = " ".join(str(text).split())
    if len(compact) <= limit:
        return compact
    return compact[: max(limit - 1, 0)].rstrip() + "…"
