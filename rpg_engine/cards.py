def append_route_table(conn: sqlite3.Connection, lines: list[str], location_id: str) -> None:
    ensure_visibility_sql_functions(conn)
    target_visibility_clause = entity_visibility_sql(PLAYER_VIEW, "target")
    source_visibility_clause = entity_visibility_sql(PLAYER_VIEW, "source")
    target_subtype_clause = entity_subtype_visibility_sql(PLAYER_VIEW, "target", "target_clock")
    source_subtype_clause = entity_subtype_visibility_sql(PLAYER_VIEW, "source", "source_clock")
    routes = conn.execute(
        f"""
        select r.*, target.name as target_name
        from routes r
        join entities target on target.id = r.to_location_id
        left join clocks target_clock on target_clock.entity_id = target.id
        where r.from_location_id = ?
          and {normalized_text_sql("target.type")} = 'location'
          and {entity_not_archived_sql("target")}
          {target_visibility_clause}
          {target_subtype_clause}
        union all
        select r.*, source.name as target_name
        from routes r
        join entities source on source.id = r.from_location_id
        left join clocks source_clock on source_clock.entity_id = source.id
        where r.to_location_id = ?
          and {normalized_text_sql("source.type")} = 'location'
          and {entity_not_archived_sql("source")}
          {source_visibility_clause}
          {source_subtype_clause}
        order by travel_minutes, target_name
        """,
        (location_id, location_id),
    ).fetchall()
    if not routes:
        return
    lines.extend(["", "## 结构化路线", "| 方向 | 地点 | 耗时 | 难度 | 危险 | 需求 |", "|------|------|------|------|------|------|"])
    for route in routes:
        if route["from_location_id"] == location_id:
            direction = "去"
            other_id = route["to_location_id"]
        else:
            direction = "返"
            other_id = route["from_location_id"]
        hidden_ids = hidden_entity_ids(conn)
        hazards = redact_hidden_refs(parse_json(route["hazards_json"], []), hidden_ids)
        requirements = redact_hidden_refs(parse_json(route["requirements_json"], []), hidden_ids)
        lines.append(
            f"| {direction} | `{other_id}` {route['target_name']} | "
            f"{route['travel_minutes']}分钟 | {format_value(route['difficulty'])} | "
            f"{format_value(hazards)} | {format_value(requirements)} |"
        )


def append_clock_card(conn: sqlite3.Connection, lines: list[str], entity: sqlite3.Row) -> None:
    clock = conn.execute("select * from clocks where entity_id = ?", (entity["id"],)).fetchone()
    if not clock:
        return
    hidden_ids = hidden_entity_ids(conn)
    filled = int(clock["segments_filled"])
    total = int(clock["segments_total"])
    bar = "■" * filled + "□" * max(total - filled, 0)
    trigger = redact_hidden_refs(clock["trigger_when_full"] or "", hidden_ids) or ""
    lines.extend(
        [
            "",
            "## 进度钟",
            "| 字段 | 值 |",
            "|------|----|",
            f"| 类型 | {display_label('clock_type', clock['clock_type'])} |",
            f"| 进度 | {bar} {filled}/{total} |",
            f"| 可见性 | {display_label('visibility', clock['visibility'])} |",
            f"| 满格触发 | {trigger} |",
            f"| 上次推进 | {clock['last_ticked_turn_id'] or '未知'} |",
        ]
    )
    append_mapping(lines, "## 推进规则", redact_hidden_refs(parse_json(clock["tick_rules_json"], {}), hidden_ids), exclude=set())


